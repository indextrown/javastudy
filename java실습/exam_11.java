public class 클래스명 {
    public static void main(String[] args) {
        int n = 1;
        while (n < 4) {
            System.out.println(n);
            n++; 
        }
        System.out.println("END");
    }
}
//for 가 가지는 세번째 조건이 while 마지막의 바로 윚줄에 붙는다
//우리는 주로 data값을 초기화 시킬 일이 없으므로 for문을 사용하는게 적절
//data파일은 행/열로 이루어져 있음